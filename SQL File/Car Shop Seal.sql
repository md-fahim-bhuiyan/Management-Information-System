/****** Script for SelectTopNRows command from SSMS  ******/
SELECT TOP (1000) [Segment]
      ,[Country]
      ,[Car Model]
      ,[Units Sold]
      ,[Manufacturing Price]
      ,[Sale Price]
      ,[Gross Sales]
      ,[Discounts]
      ,[Sales]
      ,[COGS]
      ,[Profit]
      ,[Date]
      ,[Month Number]
      ,[Month Name]
      ,[Year]
  FROM [Car_Shop].[dbo].[CAR]


